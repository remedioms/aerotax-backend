_STARTS = [r[0] for r in _RANGES]


def country_for_hex(hexid):
    """hex (str) → dict(iso, name, flag) oder None. Reine Offline-Bereichssuche."""
    if not hexid:
        return None
    try:
        v = int(str(hexid).strip(), 16)
    except (ValueError, TypeError):
        return None
    import bisect
    i = bisect.bisect_right(_STARTS, v) - 1
    if 0 <= i < len(_RANGES):
        start, end, iso, name, flag = _RANGES[i]
        if start <= v <= end:
            return {"iso": iso, "name": name, "flag": flag}
    return None
